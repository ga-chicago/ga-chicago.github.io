puts 'The type of a string named "bob" is'
puts 'bob'.class

puts 'The type of a Fixnum "5" is'
puts 5.class

puts 'the type of a Float "3.14" is'
puts 3.14.class

puts 'the type of an Array "[1,2,3]" is'
puts [1,2,3].class

puts 'the type of a Symbol ":name" is'
puts :name.class

puts 'the type of a True "true" is'
puts true.class

puts 'the type of a False "false" is'
puts false.class

hash = {:name => 'Brad'}
puts 'the type of a Hash "{:name => "Brad"}" is'
puts hash.class
