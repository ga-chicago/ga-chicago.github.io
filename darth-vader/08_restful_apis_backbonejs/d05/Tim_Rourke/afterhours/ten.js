function storyTime(noun, verb, adverb, adjective, number) {
	return '' + number + ' huge ' + adjective + ' police officers were driving ' + adverb + ' for ' + number + ' hours to try and ' + verb + ' the greased up naked deaf guy.';
}

storyTime('dog', 'smear', 'funkily', 'paisley', 8);